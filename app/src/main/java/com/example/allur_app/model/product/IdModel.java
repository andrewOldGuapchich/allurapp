package com.example.allur_app.model.product;

public class IdModel {
    int idCount;
    int idSelect;

    public IdModel(int idCount, int idSelect) {
        this.idCount = idCount;
        this.idSelect = idSelect;
    }

    public int getIdCount() {
        return idCount;
    }

    public int getIdSelect() {
        return idSelect;
    }
}
